        Scanner sc = new Scanner(System.in);
		
		System.out.print("국어점수를 입력해 주세요 : ");
		int kor = sc.nextInt();
		System.out.print("영어점수를 입력해 주세요 : ");
		int eng = sc.nextInt();
		System.out.print("수학 점수를 입력해 주세요 : ");
		int math = sc.nextInt();
		
		int tot = kor + eng + math;
		int avg = tot / 3;
		
	    String str = (((kor > 40 && eng > 40 && math > 40) && avg >= 60) ? "합격" : "불합격");
		System.out.println("국어 :" + kor);
